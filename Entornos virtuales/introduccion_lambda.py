# Lambda: Funciones sin nombre, permite crear funciones simples con tantos parametros como sea posible,pero con una unica expresion.
suma = lambda x, y: x + y
operacio_1 = suma(5, 30)
operacio_2 = suma(3, 7)
print(operacio_1)
print(operacio_2)
# Se utilizan donde una funcion normal es excesiva o inncesaria, facilita el codigo y evita la anidacion.




