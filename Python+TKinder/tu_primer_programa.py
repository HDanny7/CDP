# Primer programa vacion hecho con tkinder.
# Importamos tkinter
import tkinter as tk

# Se crea el objeto de ventana principal, se puede llamar root o ventana
ventana = tk.Tk()

# Titulo de la ventana
ventana.title('Mi primer programa con tkinter')

# Dimensiones para la ventana
ventana.geometry('500x300')

# Bucle para que el programa se mantenga ejecutado
ventana.mainloop()


