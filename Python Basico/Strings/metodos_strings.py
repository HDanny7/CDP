# Metodo capitalize: pone la primera letra del string en mayuscula
frase="camino a traves del mundo"
frase_convertida=frase.capitalize()
print(frase_convertida)

# Metodo upper: convierte un string completo en myuscula.
frase1="camino a traves del mundo"
frase_convertida1=frase1.upper()
print(frase_convertida1)

# Metodo lower: Poner todo en minuscula
frase2="CAMINO A TRAVES DEL MUNDO"
frase_convertida2=frase2.lower()
print(frase_convertida2)